package com.hartrusion.rbmksim.gui;

import java.io.PrintWriter;
import java.io.StringWriter;
import javax.swing.JOptionPane;
import javax.swing.JScrollPane;
import javax.swing.JTextArea;

/**
 * Displays an exception as a popup.
 *
 * @author Viktor Alexander Hartung
 */
public class ExceptionPopup {

    public static void show(Exception e) {

        javax.swing.SwingUtilities.invokeLater(new Runnable() {
            @Override
            public void run() {
                // Code generated by Microsoft Copilot Chat
                StringWriter sw = new StringWriter();
                e.printStackTrace(new PrintWriter(sw));
                String exceptionText = sw.toString();
                // remove tabstops on new line, ?m activates multiline 
                String cleanedText = exceptionText.replaceAll("(?m)^\\t+", "- ");
                JTextArea textArea = new JTextArea(cleanedText);
                textArea.setEditable(false);
                JScrollPane scrollPane = new JScrollPane(textArea);
                scrollPane.setPreferredSize(new java.awt.Dimension(700, 300));
                JOptionPane.showMessageDialog(null, scrollPane, 
                        "Exception - simulation terminated.",
                        JOptionPane.ERROR_MESSAGE);
            }
        });


    }
}
